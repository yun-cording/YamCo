package com.yamco.user.model.vo;

public class Random_VO {
	private String food_img , food_title;
	private String food_idx , food_status , food_date ;
	
	public String getFood_img() {
		return food_img;
	}
	public void setFood_img(String food_img) {
		this.food_img = food_img;
	}
	public String getFood_title() {
		return food_title;
	}
	public void setFood_title(String food_title) {
		this.food_title = food_title;
	}
	public String getFood_idx() {
		return food_idx;
	}
	public void setFood_idx(String food_idx) {
		this.food_idx = food_idx;
	}
	public String getFood_status() {
		return food_status;
	}
	public void setFood_status(String food_status) {
		this.food_status = food_status;
	}
	public String getFood_date() {
		return food_date;
	}
	public void setFood_date(String food_date) {
		this.food_date = food_date;
	}

	
	
}
